import math
A,B=input().split()
A=float(A)
B=float(B)
P=(B*B)-(A*A)
C=math.sqrt(P)
keliling=A+B+C
luas=0.5*C*A
print("Alas =", round(C),"cm")
print("Tinggi =", round(A),"cm")
print("Keliling =", round(keliling),"cm")
print("Luas =", round(luas),"cm^2\n")
